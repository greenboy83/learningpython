# Quotation Manipulation

quote = "   I think there is a world market for maybe five computers."

print("Orignal quote:")
print(quote)

print("\nIn uppercase:")
print(quote.upper())

print(quote.lower())

print("\nTitle:")
print(quote.title())

print("\nStrip:")
print(quote.strip())

print("\nWith a minor replacement:")
print(quote.replace("five", "millions of"))
